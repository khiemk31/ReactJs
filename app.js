const Form = {
  Input() {
    return <input />;
  },
  Checkbox() {
    return <input type="checkbox" />;
  },
};

//UI component (ko xử lý sự kiện trong UI component)
const CourseItem = ({ course, onClick }) => (
  <div>
    <h2 onClick={() => onClick(course)}>{course.title}</h2>
    <img src={course.image_url} />
    <p>{course.description}</p>
    <p>{course.published_at}</p>
  </div>
);
function Button({ title, href, onClick }) {
  let Component = "button";
  const props = {};
  if (href) {
    Component = "a";
    props.href = href;
  }
  if (onClick) {
    props.onClick = onClick;
  }
  return <Component {...props}>{title}</Component>;
}
//Xử lý DOM events
function App() {
  //useCallback
  const handelClick = (course) => {
    alert(course.title);
  };
  return (
    <div id="wrapper">
      <Button
        title="Click to"
        href="https://github.com"
        onClick={() => {
          console.log(Math.random());
        }}
      />
      <Form.Checkbox />
      <Form.Input />

      <button onDoubleClick={() => console.log(Math.random())}>Click me</button>

      {DATA.map((course) => (
        <CourseItem key={course.id} course={course} onClick={handelClick} />
      ))}
    </div>
  );
}
ReactDOM.render(<App />, document.getElementById("app"));
